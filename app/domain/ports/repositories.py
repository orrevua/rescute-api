from abc import ABC, abstractmethod
from uuid import UUID

from app.domain.entities.adoption import AdoptionApplication
from app.domain.entities.cat import Cat
from app.domain.entities.donation import DonationIntent, DonationPost
from app.domain.entities.foster import FosterApplication
from app.domain.entities.partner import Partner, PartnerNegotiation
from app.domain.entities.user import FosterProfile, Host, ProtectorProfile, User


class UserRepository(ABC):
    @abstractmethod
    async def save(self, user: User) -> User: ...
    @abstractmethod
    async def find_by_email(self, email: str) -> User | None: ...
    @abstractmethod
    async def find_by_id(self, id: UUID) -> User | None: ...
    @abstractmethod
    async def increment_token_version(self, user_id: UUID) -> None: ...
    @abstractmethod
    async def save_protector_profile(self, profile: ProtectorProfile) -> ProtectorProfile: ...
    @abstractmethod
    async def save_foster_profile(self, profile: FosterProfile) -> FosterProfile: ...
    @abstractmethod
    async def find_protector_profile(self, user_id: UUID) -> ProtectorProfile | None: ...
    @abstractmethod
    async def find_foster_profile(self, user_id: UUID) -> FosterProfile | None: ...
    @abstractmethod
    async def update_protector_profile(self, profile: ProtectorProfile) -> ProtectorProfile: ...
    @abstractmethod
    async def update_foster_profile(self, profile: FosterProfile) -> FosterProfile: ...
    @abstractmethod
    async def find_hosts(self) -> list[Host]: ...


class CatRepository(ABC):
    @abstractmethod
    async def save(self, cat: Cat) -> Cat: ...
    @abstractmethod
    async def find_by_id(self, id: UUID) -> Cat | None: ...
    @abstractmethod
    async def find_all(self, filters: dict) -> list[Cat]: ...
    @abstractmethod
    async def delete(self, id: UUID) -> None: ...
    @abstractmethod
    async def find_by_protector(self, protector_id: UUID) -> list[Cat]: ...


