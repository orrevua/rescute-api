class PartnerRepository(ABC):
    @abstractmethod
    async def save(self, partner: Partner) -> Partner: ...
    @abstractmethod
    async def find_all(self) -> list[Partner]: ...
    @abstractmethod
    async def find_by_id(self, id: UUID) -> Partner | None: ...
